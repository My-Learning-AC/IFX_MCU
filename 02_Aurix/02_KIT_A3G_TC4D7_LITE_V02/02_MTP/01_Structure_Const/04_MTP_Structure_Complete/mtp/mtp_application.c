#define SERIALIO_STRLEN         1024
#define UART_BAUDRATE           115200
#define UART_INT_PRIO_RX        (4)

#define PRINT_STRING( x )    fprintf(stderr, x)
#define PRINTF_STRING(...) { do {sprintf(SERIALIO.str, __VA_ARGS__); PRINT_STRING(SERIALIO.str);} while(0);}


typedef struct SERIALIO
{
    Ifx_ASCLIN *const asclin;
    IfxAsclin_Tx_Out *const tx_pin;
    IfxAsclin_Rx_In *const rx_pin;
    char str[SERIALIO_STRLEN];
} SERIALIO_t;

// Configuration of UART pin and module for TC4D7 Lite Kit
SERIALIO_t SERIALIO =
{
  .asclin = &MODULE_ASCLIN0,
  .tx_pin = &IfxAsclin0_TX_F_P14_0_OUT,
  .rx_pin = &IfxAsclin0_RXA_F_P14_1_IN
};

/* Create an instance of the ASC handle */
static IfxAsclin_Asc ascHandle;


IFX_INTERRUPT(uartRxIsr, 0, UART_INT_PRIO_RX);
void uartRxIsr(void)
{
    uint8 ascData;
    uint8 count;

    count = IfxAsclin_getRxFifoFillLevel(ascHandle.asclin);

    while(count > 0)
    {
        IfxAsclin_read8(ascHandle.asclin, &ascData, 1);
        UART_Get_Char(ascData);
        count--;
    }
}


void SERIALIO_Init(void)
{

    /* Initialize an instance of IfxAsclin_Asc_Config with default values */
    IfxAsclin_Asc_Config ascConfig;
    IfxAsclin_Asc_initModuleConfig(&ascConfig, SERIALIO.asclin);

    ascConfig.baudrate.baudrate = (float32)UART_BAUDRATE;                            /* Set the desired baud rate         */
    ascConfig.baudrate.oversampling         = IfxAsclin_OversamplingFactor_16;
    ascConfig.bitTiming.medianFilter        = IfxAsclin_SamplesPerBit_three;
    ascConfig.bitTiming.samplePointPosition = IfxAsclin_SamplePointPosition_8;

    /* ISR priorities and interrupt target */
    ascConfig.interrupt.rxPriority    = UART_INT_PRIO_RX;


    /* Pin configuration */
    const IfxAsclin_Asc_Pins pins = {
        NULL_PTR, IfxPort_InputMode_pullUp,                                     /* CTS pin not used                  */
        SERIALIO.rx_pin, IfxPort_InputMode_pullUp,                              /* RX pin                            */
        NULL_PTR, IfxPort_OutputMode_pushPull,                                  /* RTS pin not used                  */
        SERIALIO.tx_pin, IfxPort_OutputMode_pushPull,                           /* TX pin                            */
        IfxPort_PadDriver_cmosAutomotiveSpeed1};
    ascConfig.pins = &pins;

    /* Initialize module with above parameters */
    IfxAsclin_Asc_initModule(&ascHandle, &ascConfig);

    /* Set the Transmit FIFO Level Flag (TFL) via the FLAGSSET register to start the transmission */
    SERIALIO.asclin->FLAGSSET.B.TFLS = 1;
}

#ifdef __TASKING__

/* Retarget for Tasking compiler */
void _io_putc (char character)
{
    while (IfxAsclin_getTxFifoFillLevelFlagStatus(SERIALIO.asclin) != TRUE)
    {}

    IfxAsclin_clearTxFifoFillLevelFlag(SERIALIO.asclin);

    /* Send the character */
    IfxAsclin_writeTxData(SERIALIO.asclin, character);
}

#endif /* __TASKING__ */






