#define ISR_PRIORITY_ATOM   1                        /* Interrupt priority number                                    */
#define ATOM_FREQ           2.0f                     /* ATOM frequency                                               */
#define CMU_FREQ            1000000.0f               /* CMU clock frequency                                          */
#define LED                 &MODULE_P03, 9           /* LED which will be toggled in Interrupt Service Routine (ISR) */


IfxEgtm_Atom_Timer           g_timerDriver;          /* ATOM driver                                                  */
static uint8               wait_time = 0;


/* This function initializes the ATOM */
void Init_Timer(void)
{
    IfxEgtm_enable(&MODULE_EGTM);                                                /* Enable EGTM                      */

    IfxEgtm_Atom_Timer_Config timerConfig;                                       /* Timer configuration structure    */
    IfxEgtm_Atom_Timer_initConfig(&timerConfig, &MODULE_EGTM);                   /* Initialize default parameters    */

    timerConfig.cluster = IfxEgtm_Cluster_0;                                     /* Select the Cluster 0             */
    timerConfig.timerChannel = IfxEgtm_Atom_Ch_0;                                /* Select ATOM channel 0            */
    timerConfig.clock = IfxEgtm_Atom_Ch_ClkSrc_cmuclk0;                          /* Select the CMU clock 0           */
    timerConfig.frequency = ATOM_FREQ;                                           /* Set timer frequency              */
    timerConfig.interrupt.isrPriority = ISR_PRIORITY_ATOM;                       /* Set interrupt priority           */
    timerConfig.interrupt.isrProvider = IfxSrc_Tos_cpu0;                         /* Set interrupt provider           */

    IfxEgtm_Cmu_setClkFrequency(&MODULE_EGTM, IfxEgtm_Cmu_Clk_0, CMU_FREQ);      /* Set the clock frequency          */
    IfxEgtm_Cmu_enableClocks(&MODULE_EGTM, IFXEGTM_CMU_CLKEN_CLK0);              /* Enable the CMU clock 0           */
    IfxEgtm_Atom_Timer_init(&g_timerDriver, &timerConfig);                       /* Initialize the ATOM              */

    IfxPort_setPinModeOutput(LED, IfxPort_OutputMode_pushPull, IfxPort_OutputIdx_general);  /* Set LED pin mode      */
}

void Start_Timer(uint8 tick_count_in_sec)
{
    wait_time = tick_count_in_sec;
    IfxEgtm_Atom_Timer_run(&g_timerDriver);                                      /* Start the ATOM                   */
}


void Stop_Timer(void)
{
    IfxEgtm_Atom_Timer_stop(&g_timerDriver);                                      /* Stop the ATOM                   */
}

/* Macro to define the Interrupt Service Routine */
IFX_INTERRUPT(interruptHandlerEgtmAtom, 0, ISR_PRIORITY_ATOM);

/* Interrupt Service Routine of the ATOM */
void interruptHandlerEgtmAtom(void)
{

    IfxPort_togglePin(LED);                                                      /* Toggle the LED                   */
    IfxEgtm_Atom_Timer_acknowledgeTimerIrq(&g_timerDriver);                      /* Reset the timer event            */

    static uint8 period_time = 0;

    if(period_time == (wait_time/2))
    {
        printf("ins:Time Period is going to expire\n");
        printf("ins: Please complete the test soon\n");
    }
    else if(period_time == wait_time)
    {
        Stop_Timer();
        period_time = 0;
        wait_time = 0;
        printf("ins: Test Time period expired\n");
    }

    period_time++;

}






